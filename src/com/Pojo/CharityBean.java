package com.Pojo;

public class CharityBean {

	String CharityName,CharityDescription,CharityLogo;
	int CharityId;
	public String getCharityName() {
		return CharityName;
	}
	public void setCharityName(String charityName) {
		CharityName = charityName;
	}
	public String getCharityDescription() {
		return CharityDescription;
	}
	public void setCharityDescription(String charityDescription) {
		CharityDescription = charityDescription;
	}
	public String getCharityLogo() {
		return CharityLogo;
	}
	public void setCharityLogo(String charityLogo) {
		CharityLogo = charityLogo;
	}
	public int getCharityId() {
		return CharityId;
	}
	public void setCharityId(int charityId) {
		CharityId = charityId;
	}
	
	
}
